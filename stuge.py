def default_loop(state):
    return state


@dataclass
class StgeState:
    frame_time_target: int = 0
    delta_time: float = 0.0
    frame_start: float = 0.0
    keys: list[str] = field(default_factory=list)
    loop_fun: Callable[[Any], Any] = default_loop


_state = StgeState()


def size() -> tuple[int, int]:
    return turtle.screensize()


def quit():
    """Quit the program"""
    sys.exit()


def keypresses() -> list[str]:
    return _state.keys


def delta_time() -> float:
    return _state.delta_time


def set_loop(loop: Callable[[Any], Any]):
    _state.loop_fun = loop


def run(setup: Callable[[], Any], loop: Callable[[Any], Any], fps: int = 30):
    _state.loop_fun = loop
    frame_time_target = int(1 / fps * 1000)
    turtle.speed(0)
    turtle.tracer(False)
    turtle.hideturtle()
    turtle.onclick(turtle.listen)

    def key_handler(key):
        _state.keys.append(key)

    for k in KEYS:
        turtle.onkeypress(lambda key=k: key_handler(key), str(k))

    turtle.listen()
    state = setup()

    def frame():
        nonlocal state
        turtle.clear()
        turtle.home()
        state = _state.loop_fun(state)
        _state.keys = []
        turtle.update()
        try:
            turtle.ontimer(frame, frame_time_target)
        except turtle.Terminator:
            pass

    frame()
    turtle.mainloop()
